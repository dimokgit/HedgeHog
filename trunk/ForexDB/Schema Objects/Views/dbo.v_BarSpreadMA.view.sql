CREATE VIEW dbo.v_BarSpreadMA
AS
SELECT     Pair, Period, StartDate, dbo.MA(Pair, Period, StartDate, 120) * 10000 AS SpreadMA, dbo.MA_H1(Pair, Period, StartDate, 120) * 10000 AS SpreadMA_H1, 
                      dbo.MA(Pair, Period, StartDate, 60) * 10000 AS SpreadMAShort, dbo.MA_H1(Pair, Period, StartDate, 60) * 10000 AS SpreadMAShort_H1, 
                      (AskHigh + AskLow + AskOpen + AskClose + BidHigh + BidLow + BidOpen + BidClose) / 8 AS Price, (AskHigh - AskLow + BidHigh - BidLow) 
                      / 2 AS Spread
FROM         dbo.t_Bar AS B
WHERE     (Pair = N'EUR/USD')

GO
